import math

print(math.sqrt(25)) # Output: 5.0
print(math.floor(5.7)) # Output: 5
print(math.ceil(5.7)) # Output: 6
print(math.floor(-5.7)) # Output: -6
print(math.ceil(-5.7)) # Output: -5
print(math.pi) # Output: 3.141592653589793
print(math.e) # Output: 2.718281828459045
print(math.factorial(5)) # Output: 120

print(math.gcd(12, 15)) # Output: 3
print(math.lcm(12, 15)) # Output: 60

print(math.pow(2, 3)) # Output: 8.0

print(math.exp(1)) # Output: 2.718281828459045
print(math.log(10)) # Output: 2.302585092994046

