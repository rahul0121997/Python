# Create a dictionary with 6 key-value pairs
dictionary = {
    'name': 'Alice',
    'age': 25,
    'city': 'New York',
    'profession': 'Engineer',
    'hobby': 'Painting',
    'language': 'Python'
}

# Display the dictionary
print("Dictionary:", dictionary)

# Access values using dictionary keys
name = dictionary['name']
age = dictionary['age']
city = dictionary['city']
profession = dictionary['profession']
hobby = dictionary['hobby']
language = dictionary['language']

# Display accessed values
print(f"Name: {name}")
print(f"Age: {age}")
print(f"City: {city}")
print(f"Profession: {profession}")
print(f"Hobby: {hobby}")
print(f"Language: {language}")
